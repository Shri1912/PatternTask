fun main() {
    var number1=10
    var number2=30

    println("----Before Swapping----")
    println("Number 1: $number1")
    println("Number 2: $number2")

    var temp=number1
    number1=number2
    number2=temp

    println("----After Swapping----")
    println("Number 1: $number1")
    println("Number 2: $number2")

}